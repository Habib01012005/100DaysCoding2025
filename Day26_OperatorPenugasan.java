public class Day26_OperatorPenugasan {
    public static void main(String[] args) {
        int angka1 = 20;
        int angka2 = 5;

        angka1 += angka2;
        System.out.println(angka1);

        angka1 -= angka2;
        System.out.println(angka1);

        angka1 *= angka2;
        System.out.println(angka1);

        angka1 /= angka2;
        System.out.println(angka1);

        angka1 %= angka2;
        System.out.println(angka1);
        
    }
}
