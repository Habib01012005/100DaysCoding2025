public class Day05_BilanganBulat {
    public static void main(String[] args) {
        byte angka1 = 10;
        short angka2 = 20;
        int angka3 = 30;
        long angka4 = 40L;

        System.out.println("Nilai byte  : " + angka1);
        System.out.println("Nilai short : " + angka2);
        System.out.println("Nilai int   : " + angka3);
        System.out.println("Nilai long  : " + angka4);
    }
}
