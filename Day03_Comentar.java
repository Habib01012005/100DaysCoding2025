public class Day03_Comentar {
    public static void main(String[] args) {
        // Menampilkan teks ke konsol
        System.out.println("Penjumlahan");

        // Deklarasi variabel untuk menyimpan angka
        int angka1 = 10; // Angka pertama
        int angka2 = 20; // Angka kedua
        int hasil = angka1 + angka2; // Menjumlahkan angka1 dan angka2

        // Menampilkan hasil penjumlahan ke konsol
        System.out.println("Hasil dari " + angka1 + " + " + angka2 + " = " + hasil);
    }   
}
